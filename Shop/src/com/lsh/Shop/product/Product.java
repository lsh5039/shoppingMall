package com.lsh.Shop.product;

public class Product {
	private int p_num;
	private String p_name;
	private String p_price;
	private String p_category;
	private String p_file;
	private String p_realfile;
	private int p_event;
	private int p_new;
	private int p_discount;
	public int getP_num() {
		return p_num;
	}
	public void setP_num(int p_num) {
		this.p_num = p_num;
	}
	public String getP_name() {
		return p_name;
	}
	public void setP_name(String p_name) {
		this.p_name = p_name;
	}
	public String getP_price() {
		return p_price;
	}
	public void setP_price(String p_price) {
		this.p_price = p_price;
	}
	
	
	public String getP_category() {
		return p_category;
	}
	public void setP_category(String p_category) {
		this.p_category = p_category;
	}
	public String getP_file() {
		return p_file;
	}
	public void setP_file(String p_file) {
		this.p_file = p_file;
	}
	public String getP_realfile() {
		return p_realfile;
	}
	public void setP_realfile(String p_realfile) {
		this.p_realfile = p_realfile;
	}
	public int getP_event() {
		return p_event;
	}
	public void setP_event(int p_event) {
		this.p_event = p_event;
	}
	public int getP_new() {
		return p_new;
	}
	public void setP_new(int p_new) {
		this.p_new = p_new;
	}
	public int getP_discount() {
		return p_discount;
	}
	public void setP_discount(int p_discount) {
		this.p_discount = p_discount;
	}
	
	
}
